"""
This module contains the object representations of Talkie types.
"""


class DataType(object):

    def __init__(self):
        super(DataType, self).__init__()


class Number(object):
    def __init__(self):
        super(Number, self).__init__()


class Integer(Number):
    def __init__(self):
        super(Integer, self).__init__()


class Float(Number):
    def __init__(self):
        super(Float, self).__init__()
